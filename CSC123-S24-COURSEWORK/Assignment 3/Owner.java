public class Owner
{
	String firstName;
	String lastName;
	String address;
	String city;
	String state;
	String zip;

	public Owner(String firstName, String lastName, String address, String city, String state, String zip)
	{
		this.firstName = firstName;
		this.lastName = lastName;
		this.address = address;
		this.city = city;
		this.state = state;
		this.zip = zip;
	}
	

}
